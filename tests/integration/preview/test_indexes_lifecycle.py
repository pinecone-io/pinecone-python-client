"""Integration tests for preview index lifecycle (create, configure, delete).

Tests run against Pinecone API version 2026-01.alpha. These tests make real
API calls and skip gracefully when the preview endpoint is unavailable.

Covers §13 end-to-end examples from spec/preview.md:
- Multi-field schema with all field types
- Auto-generated index name
- Schema evolution (adding fields to an existing index)
- Basic lifecycle smoke: create / describe / list / delete
"""

from __future__ import annotations

import contextlib
from typing import Any

import pytest

from pinecone import Pinecone
from pinecone.errors import ForbiddenError, NotFoundError, PineconeValueError
from pinecone.preview import PreviewSchemaBuilder
from pinecone.preview.models import PreviewIndexModel
from tests.integration.conftest import poll_until

pytestmark = [pytest.mark.integration, pytest.mark.preview_integration]

# Sentinel returned by _check_deleted() when NotFoundError is raised.
_DELETED = object()


def _simple_fts_schema() -> dict[str, Any]:
    """Minimal single-field FTS schema used as a lightweight create payload."""
    return PreviewSchemaBuilder().add_string_field("text", full_text_searchable=True).build()


def _is_ready(m: object) -> bool:
    return isinstance(m, PreviewIndexModel) and m.status.state == "Ready"


# ---------------------------------------------------------------------------
# TestMultiFieldSchema — §13 "Multi-field schema with all field types"
# ---------------------------------------------------------------------------


class TestMultiFieldSchema:
    """Index with all six supported field types creates successfully."""

    def test_create_index_with_all_field_types(
        self,
        client: Pinecone,
        preview_index_name: str,
        cleanup_preview_indexes: list[str],
        require_preview: None,
    ) -> None:
        """Create an index with 6 field types; assert name, host, and schema.fields count."""
        schema = (
            PreviewSchemaBuilder()
            .add_dense_vector_field("embedding", dimension=1536, metric="cosine")
            .add_sparse_vector_field("sparse")
            .add_semantic_text_field("content", model="multilingual-e5-large")
            .add_string_field("title", full_text_searchable=True, language="en")
            .add_string_field("category", filterable=True)
            .add_integer_field("year")
            .build()
        )
        cleanup_preview_indexes.append(preview_index_name)
        client.preview.indexes.create(name=preview_index_name, schema=schema)

        poll_until(
            lambda: client.preview.indexes.describe(preview_index_name),
            _is_ready,
            timeout=300,
            interval=5,
            description=f"index {preview_index_name} ready",
        )

        model = client.preview.indexes.describe(preview_index_name)
        assert model.name == preview_index_name
        assert isinstance(model.host, str)
        assert len(model.host) > 0
        assert len(model.schema.fields) == 6


