# TestSchemaEvolution — §13 "Schema evolution"
# ---------------------------------------------------------------------------


class TestSchemaEvolution:
    """configure() with an additive schema adds a field to an existing index."""

    def test_configure_adds_field_to_existing_schema(
        self,
        client: Pinecone,
        preview_index_name: str,
        cleanup_preview_indexes: list[str],
        require_preview: None,
    ) -> None:
        """Create 1-field index, configure to add a 2nd field, poll until schema has 2 fields."""
        initial_schema = (
            PreviewSchemaBuilder().add_string_field("text", full_text_searchable=True).build()
        )
        cleanup_preview_indexes.append(preview_index_name)
        client.preview.indexes.create(name=preview_index_name, schema=initial_schema)

        poll_until(
            lambda: client.preview.indexes.describe(preview_index_name),
            _is_ready,
            timeout=300,
            interval=5,
            description=f"index {preview_index_name} ready before configure",
        )

        evolved_schema = (
            PreviewSchemaBuilder()
            .add_string_field("text", full_text_searchable=True)
            .add_dense_vector_field("embedding", dimension=384, metric="cosine")
            .build()
        )
        client.preview.indexes.configure(preview_index_name, schema=evolved_schema)

        def _has_two_fields(m: object) -> bool:
            return isinstance(m, PreviewIndexModel) and len(m.schema.fields) == 2

        poll_until(
            lambda: client.preview.indexes.describe(preview_index_name),
            _has_two_fields,
            timeout=300,
            interval=5,
            description=f"index {preview_index_name} schema evolved to 2 fields",
        )

        described = client.preview.indexes.describe(preview_index_name)
        assert len(described.schema.fields) == 2


