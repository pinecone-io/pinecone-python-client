class TestLifecycleBasics:
    """Smoke tests verifying the full control-plane lifecycle."""

    def test_describe_after_create_returns_same_model(
        self,
        client: Pinecone,
        preview_index_name: str,
        cleanup_preview_indexes: list[str],
        require_preview: None,
    ) -> None:
        """describe() returns a model with matching name, non-empty host, and a schema dict."""
        cleanup_preview_indexes.append(preview_index_name)
        client.preview.indexes.create(name=preview_index_name, schema=_simple_fts_schema())

        poll_until(
            lambda: client.preview.indexes.describe(preview_index_name),
            _is_ready,
            timeout=300,
            interval=5,
            description=f"index {preview_index_name} ready",
        )

        described = client.preview.indexes.describe(preview_index_name)
        assert described.name == preview_index_name
        assert isinstance(described.host, str)
        assert len(described.host) > 0
        assert isinstance(described.schema.fields, dict)

    def test_list_includes_created_index(
        self,
        client: Pinecone,
        preview_index_name: str,
        cleanup_preview_indexes: list[str],
        require_preview: None,
    ) -> None:
        """list() includes the created index by name after it becomes Ready."""
        cleanup_preview_indexes.append(preview_index_name)
        client.preview.indexes.create(name=preview_index_name, schema=_simple_fts_schema())

        poll_until(
            lambda: client.preview.indexes.describe(preview_index_name),
            _is_ready,
            timeout=300,
            interval=5,
            description=f"index {preview_index_name} ready",
        )

        names = [idx.name for idx in client.preview.indexes.list()]
        assert preview_index_name in names

    def test_delete_removes_index(
        self,
        client: Pinecone,
        preview_index_name: str,
        require_preview: None,
    ) -> None:
        """delete() removes the index; subsequent describe raises NotFoundError."""
        try:
            client.preview.indexes.create(name=preview_index_name, schema=_simple_fts_schema())

            poll_until(
                lambda: client.preview.indexes.describe(preview_index_name),
                _is_ready,
                timeout=300,
                interval=5,
                description=f"index {preview_index_name} ready before delete",
            )

            client.preview.indexes.delete(preview_index_name)

            def _check_deleted() -> object:
                try:
                    return client.preview.indexes.describe(preview_index_name)
                except NotFoundError:
                    return _DELETED

            poll_until(
                _check_deleted,
                lambda result: result is _DELETED,
                timeout=120,
                interval=5,
                description=f"index {preview_index_name} deleted",
            )
        except Exception:
            with contextlib.suppress(Exception):
                client.preview.indexes.delete(preview_index_name)
            raise


